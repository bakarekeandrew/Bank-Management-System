    private void searchBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchBtnActionPerformed
        // TODO add your handling code here:
        int accountNumber = Integer.parseInt(userText.getText());

        AccountDao dao = new AccountDao();
        Account searchUser = dao.retrieveAccount(accountNumber);

        if (searchUser != null) {
       
            namesText.setText(searchUser.getFirst_name() + " " + searchUser.getLast_name());
            balanceText.setText(String.valueOf(searchUser.getAmount()));

            TransactionDao daotrans = new TransactionDao();
            List<Transaction> transactions = daotrans.transactionAccount(accountNumber);
            
            if (transactions != null) {
                updateTransactionTable(transactions);
            }
//            else {
//                JOptionPane.showMessageDialog(this, "Transactions not found", "Error", JOptionPane.ERROR_MESSAGE);
//            }

            
        } else {
      
            JOptionPane.showMessageDialog(this, "User not found", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_searchBtnActionPerformed

    
    private void updateTransactionTable(List<Transaction> transactions) {
    model.setRowCount(0); // Clear existing rows
    for (Transaction trans : transactions) {
        model.addRow(new Object[]{
            trans.getTimestamp(),
            trans.getType(),
            trans.getAmount()
        });
    }
}

    
