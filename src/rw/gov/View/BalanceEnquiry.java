    public void addRows() {
    TransactionDao dao = new TransactionDao();
    model.setRowCount(0); // Clear existing rows

    try {

        List<Transaction> transactions = dao.transactionAccount();
        if (!transactions.isEmpty()) {
            for (Transaction trans : transactions) {
                model.addRow(new Object[]{
                    trans.getTimestamp(),
                    trans.getType(),
                    trans.getAmount()
                });
            }
        } else {
            JOptionPane.showMessageDialog(this, "No transactions found", "Information", JOptionPane.INFORMATION_MESSAGE);
        }
    } catch (Exception ex) {
        ex.printStackTrace();
//        JOptionPane.showMessageDialog(this, "Error fetching transactions", "Error", JOptionPane.ERROR_MESSAGE);
    }
}




